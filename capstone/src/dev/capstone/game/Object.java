package dev.capstone.game;

public class Object
{
	private int x;
	private int y;
	
	public Object(int x, int y)
	{
		this.y = y;
		this.x = x;
	}
	
	public int getX()
	{
		return x;
	}
	
	public int getY()
	{
		return y;
		
	}
	
	public void setX(int x)
	{
		this.x = x;
	}
	
	public void setY(int y)
	{
		this.y = y;
	}
}



